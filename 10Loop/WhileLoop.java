
public class WhileLoop {

    public static void main(String[] args) {
        
        int num = 1; //initialization
            while (num <= 10) { // COndition 
                System.out.print("Sujit Tomar"); // actual work
                num += 1; // update condition
            }
        

        int count = 100;
            while (count >= 10) {
                System.out.println("Sujit Tomar" + count);
                count -= 1;
                
            }

    }

}