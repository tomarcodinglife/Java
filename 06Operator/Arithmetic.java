public class Arithmetic {
    public static void main(String[] args) {
        int NumA = 5;
        int NumB = 2;

        System.out.println("NumA = " + NumA);
        System.out.println("NumB = " + NumB);

        // Aritmetic Operator
        System.out.println(NumA + NumB);
        System.out.println(NumA - NumB);
        System.out.println(NumA * NumB);
        System.out.println(NumA / NumB);
        System.out.println(NumA % NumB);


        float NumX = 5;
        float NumY = 2;

        System.out.println(NumX + NumY);
        System.out.println(NumX - NumY);
        System.out.println(NumX * NumY);
        System.out.println(NumX / NumY);
        System.out.println(NumX % NumY);


    }
}