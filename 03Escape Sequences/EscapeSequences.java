
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Sujit \n Kumar \n Singh");
        System.out.println("Sujit\tKumar\tSingh");
        System.out.println("Sujit \\Tomar");
        System.out.println("\' Sujit Tomar \'");
        System.out.println("\"Sujit Tomar\"");

    }
}
