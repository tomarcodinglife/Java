
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Sujit \n Kumar \n Singh"); // Next Line Print \n
        System.out.println("Sujit\tKumar\tSingh"); //  Sujit	Kumar   Singh
        System.out.println("C:\\myFiles\\Java"); //  C:\myFiles\Java
        System.out.println("\' Sujit Tomar \'"); //  ' Sujit Tomar '
        System.out.println("\"Sujit Tomar\""); // "Sujit Tomar"

    }
}
